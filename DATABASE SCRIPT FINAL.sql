CREATE TABLE STORE
(
    storeNum VARCHAR(2) NOT NULL PRIMARY KEY,
    woking_Hours VARCHAR(50),
    mgr_ID Number(10),
    Pick_up VARCHAR(3)
);

CREATE TABLE EMPLOYEE
(
    ID NUMBER(10) NOT NULL PRIMARY KEY,
    Fname VARCHAR(20) NOT NULL,
    Lname VARCHAR(20) NOT NULL,
    Date_of_birth DATE,
    Hours_worked NUMBER(4,2),
    Shift_time VARCHAR(50),
    Salary NUMBER(10,2),
    storeNum VARCHAR(2),
    Super_ID NUMBER(10)
);

CREATE TABLE TECHNICIAN
(
    EID NUMBER(10) NOT NULL PRIMARY KEY
);

CREATE TABLE DELIVERY_PERSON
(
    EID NUMBER(10) NOT NULL PRIMARY KEY,
    driver_licenseNo VARCHAR(12) NOT NULL UNIQUE
);

CREATE TABLE CUSTOMER
(
    Phone_number NUMBER(12) NOT NULL PRIMARY KEY,
    Name VARCHAR(50),
    Date_of_birth DATE,
    City VARCHAR(50),
    Street NUMBER(5),
    Building_number NUMBER(5)
);

CREATE TABLE DELIVERY
(
    orderNo NUMBER(10) NOT NULL PRIMARY KEY,
    Estimated_date DATE,
    Delivery_fee NUMBER(5),
    Package_weight NUMBER(5),
    EID NUMBER(10)
);

CREATE TABLE MEMBERSHIP
(
    MID NUMBER(10) NOT NULL PRIMARY KEY,
    CphoneNo NUMBER(12),
    expiration_date DATE,
    points NUMBER(5)
);

CREATE TABLE SERVICES
(
    SID NUMBER(10) NOT NULL PRIMARY KEY,
    serviceType VARCHAR(50),
    duration NUMBER(2),
    cost NUMBER(4),
    EID NUMBER(10),
    CphoneNo NUMBER(12)
);

CREATE TABLE DEVICE
(
    Serial_number VARCHAR(20) NOT NULL PRIMARY KEY,
    name VARCHAR(50),
    company VARCHAR(50),
    color VARCHAR(20),
    price NUMBER(10,2),
    storage NUMBER(4),
    Edition NUMBER(10),
    sup_ID NUMBER(10)
);

CREATE TABLE PURCHASES
(
    Dserial_number VARCHAR(12) NOT NULL PRIMARY KEY,
    CPhoneNo NUMBER(12),
    p_method VARCHAR(20),
    p_time TIMESTAMP
);

CREATE TABLE SPARE_PIECES
(
    ID NUMBER(10) NOT NULL PRIMARY KEY,
    Dname VARCHAR(50),
    Dedition VARCHAR(4),
    Dcompany VARCHAR(20),
    Price NUMBER(10,2),
    sup_ID NUMBER(10)
);

CREATE TABLE SUPPLIER
(
    ID NUMBER(10) NOT NULL PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE WARRANTY
(
    Dserial_number varchar(12) NOT NULL PRIMARY KEY,
    type NUMBER(2,2) CHECK(type < 1 AND type > 0),
    Cost NUMBER(10,2),
    expiration_date DATE
);

CREATE TABLE DEGREE
(
    EID NUMBER(10) NOT NULL PRIMARY KEY,
    Tdegree VARCHAR(50) 
);

CREATE TABLE ORDERS
(
    orderNo NUMBER(10) NOT NULL,
    Dserial_number VARCHAR(20) NOT NULL,
    CPhoneNo NUMBER(12) NOT NULL ,
    PRIMARY KEY(orderNo,Dserial_number, CPhoneNo)
